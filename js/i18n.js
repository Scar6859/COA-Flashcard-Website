let currentLang = "en";
let cantoneseMode = false;

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  currentLang = lang;
}

export function getCantonese() {
  return cantoneseMode;
}

export function setCantonese(enabled) {
  cantoneseMode = enabled;
}

/** Language used for UI strings and labels */
export function getDisplayLang() {
  if (currentLang === "en") return "en";
  return cantoneseMode ? "yue" : "zh";
}

export function t(key) {
  const lang = getDisplayLang();
  return translations[lang][key] || translations.en[key] || key;
}

export function applyI18n() {
  const lang = getDisplayLang();
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    } else if (lang === "yue" && translations.zh[key]) {
      el.textContent = translations.zh[key];
    }
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (translations[lang][key]) {
      el.placeholder = translations[lang][key];
    } else if (lang === "yue" && translations.zh[key]) {
      el.placeholder = translations.zh[key];
    }
  });
  document.title = translations[lang].title || translations.zh.title;
}

export function getSetDisplayName(set) {
  if (currentLang === "en") return set.nameEn;
  if (cantoneseMode) return set.nameYue || set.nameZh;
  return set.nameZh;
}

